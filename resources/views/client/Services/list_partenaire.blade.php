                                <span class="m-widget4__title">${partner.name}</span>
                                <br>
                                <span class="m-widget4__sub">${partner.rate} dhs/h</span>
                            </div>
                            <div class="m-widget4__progress">
                                <div class="m-widget4__progress-wrapper">
                                    <span class="m-widget17__progress-number">${partner.availability}</span>
                                    <div class="progress m-progress--sm">
                                        <div class="progress-bar bg-danger" role="progressbar" style="width: 63%;" aria-valuenow="25" aria-valuemin="0" aria-valuemax="63"></div>
